saldo = 200
saque = 450

print(saldo == saque) #Verifica se o valor das variáveis é o mesmo
print(saldo != saque) #Verifica se o valor das variáveis é diferente
print(saldo > saque) #Verifica se o valor da var saldo é maior do que o da var saque
print(saldo >= saque) #Verifica se o valor da var saldo é maior ou igual ao da var saque
print(saldo < saque) #Verifica se o valor da var saldo é menor do que o da var saque
print(saldo <= saque) #Verifica se o valor da var saldo é menor ou igual ao da var saque
