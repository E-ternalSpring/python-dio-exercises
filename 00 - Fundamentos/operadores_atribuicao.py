saldo = 500
print(saldo)

saldo += 10 #Soma o valor com o valor da variável, sobrescrevendo-a 
print(saldo)

saldo -= 5 #Subtraí o valor do valor da variável, sobrescrevendo-a 
print(saldo)

saldo //= 2 #Faz uma divisão inteira do valor da variável pelo valor inserido, sobrescrevendo-a 
print(saldo)

saldo /= 2 #Faz a divisão do valor da variável pelo valor inserido, sobrescrevendo-a 
print(saldo)

saldo *= 10 #Faz uma multiplicação do valor da variável pelo valor inserido, sobrescrevendo-a 
print(saldo)

saldo %= 4.6 #Retorna o resto da divisão do valor da variável pelo valor inserido, sobrescrevendo-a 
print(saldo)

saldo **= 2 #Faz uma potenciação do valor da variável pelo valor inserido, sobrescrevendo-a 
print(saldo)
