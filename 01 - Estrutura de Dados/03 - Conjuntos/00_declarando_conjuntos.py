numeros = set([1, 2, 3, 1, 3, 4])
print(numeros)  # {1, 2, 3, 4}

letras = set("abacaxi")
print(letras)  # {'a', 'x', 'b', 'i', 'c'}

carros = set(("palio", "gol", "celta", "palio"))
print(carros)  # {'palio', 'gol', 'celta'}

flores = {"margarida", "rosa", "tulipa", "orquídea", "rosa"}
print(flores) # {'rosa', 'margarida', 'orquídea', 'tulipa'}
